def circle(radius):
    area = 3.14 * radius * radius
    print("Area of Circle:", area)

def rectangle(length, width):
    area = length * width
    print("Area of Rectangle:", area)

def triangle(base, height):
    area = 0.5 * base * height
    print("Area of Triangle:", area)

circle(5)
rectangle(4, 6)
triangle(3, 8)